# Python program to find Sum of Items in a Dictionary

# Create a dictionary
numbers = {'a': 10, 'b': 20, 'c': 30, 'd': 40}

# Find sum of all values
total = sum(numbers.values())

# Display result
print("Dictionary:", numbers)
print("Sum of all items:", total)

